package Class8;

public class E14NestedLoops {
    /*
    11
    12
    12
    21
    22
    23
     */
    public static void main(String[] args) {


        for (int j = 1; j < 3; j++) {
            for (int i = 1; i < 4; i++) {
                System.out.println(j + " " + i);
            }
            System.out.println();
        }
    }
}
